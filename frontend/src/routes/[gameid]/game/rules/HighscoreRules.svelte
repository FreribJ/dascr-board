<div class="space-y-5 text-xl">
  <div class="space-y-4">
    <p>
      In a game of Highscore the object is for one player or a team to be the
      first to reach the desired target number starting from 0.
    </p>
    <p>
      In simple terms, after three darts are thrown, the throwing player adds
      the total scored from to his current total until he reaches the target
      number. The target number must be hit on point.
    </p>
  </div>
  <div class="space-y-4">
    <h4 class="text-center underline font-semibold">Straight</h4>
    <p>
      When choosing straight no condition to start into or end the game are in
      place.
    </p>
  </div>

  <div class="space-y-4">
    <h4 class="text-center underline font-semibold">Double/Master In</h4>
    <p>
      In order to start adding points each player has to hit a double (Double
      in) or a double/triple (Master in).
    </p>
  </div>

  <div class="space-y-4">
    <h4 class="text-center underline font-semibold">Double/Master Out</h4>
    <p>
      In order to reach the target number each player must finish by throwing a
      double (Double out) or a double/triple (Master out).
    </p>
  </div>

  <div class="space-y-4">
    <h4 class="text-center underline font-semibold">Example</h4>
    <p>
      The target number is 301 and a player already has 265 points. With doulbe
      out he would have left 36 points to win, so he must hit a double 18 to end
      the game.
    </p>
  </div>

  <div class="space-y-4">
    <h4 class="text-center underline font-semibold">Elimination</h4>
    <p>
      In 'Elimination' the basic rules are the same. But, whenever a player
      scores the same number as other player(s) the other(s) will be reset to 0.
    </p>
    <p>
      So if player 1 has 250 points and player 2 starts his throw round with 248
      points and he then throws a 2 with his first dart, player 1 will be reset
      to 0 points.
    </p>
  </div>
</div>
