<div class="space-y-5 text-xl">
  <div class="space-y-4">
    <p>
      In a game of X01 the object is for one player or a team to be the first to
      reach zero from starting total of X01.
    </p>
    <p>
      In simple terms, after three darts are thrown, the throwing player
      subtracts the total scored from his current total until he reaches zero.
    </p>
  </div>
  <div class="space-y-4">
    <h4 class="text-center underline font-semibold">Straight</h4>
    <p>
      When choosing straight no condition to start into or end the game are in
      place.
    </p>
  </div>

  <div class="space-y-4">
    <h4 class="text-center underline font-semibold">Double/Master In</h4>
    <p>
      In order to start substracting points each player has to hit a double
      (Double in) or a double/triple (Master in).
    </p>
  </div>

  <div class="space-y-4">
    <h4 class="text-center underline font-semibold">Double/Master Out</h4>
    <p>
      In order to reach zero each player must finish by throwing a double
      (Double out) or a double/triple (Master out).
    </p>
  </div>

  <div class="space-y-4">
    <h4 class="text-center underline font-semibold">Example</h4>
    <p>
      If player one has 36 remaining he must hit double 18 to win, while if
      player two has 45 remaining he must hit single 5, double 20 to win.
    </p>
  </div>
</div>
