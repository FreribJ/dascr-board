<div class="space-y-5 text-xl">
    <div class="space-y-4">
        <p>
            The object of Cricket is to close all numbers from 15 to 20, and the
            bull's eye before the opponent does. To close a number, a player
            must score three of that number (any combination of singles doubles
            and triples).
        </p>
    </div>

    <div class="space-y-4">
        <h4 class="text-center underline font-semibold">Scoring & Winning</h4>
        <p>There are different variants of the game.</p>
    </div>

    <div class="space-y-4">
        <h4 class="text-center underline font-semibold">Normal</h4>
        <p>
            Once a player closes a number every subsequent score on that number
            increases their score, unless the other player has closed that
            number as well. The player who has closed all numbers and whose
            score is not lower than the opponent's, wins the game.
        </p>
    </div>

    <div class="space-y-4">
        <h4 class="text-center underline font-semibold">Cut Throat</h4>
        <p>
            Once a player closes a number every subsequent score on that number
            increases the score of other players unless the other player has
            closed that number as well. The player who has closed all numbers
            and whose score is not higher than the opponent's, wins the game.
        </p>
    </div>

    <div class="space-y-4">
        <h4 class="text-center underline font-semibold">No Score</h4>
        <p>
            No score will be tracked throughout the game. The player who has
            closed all numbers wins the game.
        </p>
    </div>

    <div class="space-y-4">
        <h4 class="text-center underline font-semibold">Options</h4>
        <p>There are a few options to choose from</p>
    </div>

    <div class="space-y-4">
        <h4 class="text-center underline font-semibold">Random Numbers</h4>
        <p>
            This option will generate 7 random numbers to hit instead of the
            classic 15-20 + 25
        </p>
    </div>

    <div class="space-y-4">
        <h4 class="text-center underline font-semibold">Ghost</h4>
        <p>
            This is a variant I invented to spice up the cricket game. Numbers
            are not shown at first. As soon as a player hits a number which was
            chosen randomly before the number will be revealed until all numbers
            are shown finally.
        </p>
    </div>
</div>
